    public void checkUserName_Password() {
        frmNhanVien nhanVien = new frmNhanVien();
        userName = txt_userName.getText();
        String passWord = txt_passWord.getText();
        for (int i = 0; i < nhanVien.listNV.size(); i++) {
            if (nhanVien.listNV.get(i).getMaNhanVien().equals(userName) && nhanVien.listNV.get(i).getMatKhau().equals(validate.md5(passWord))) {
                if (nhanVien.listNV.get(i).getTrangThai() == 0) {
                    JOptionPane.showMessageDialog(this, "Nhân viên này đã bị khóa !");
                    return;
                }

                this.dispose();
                frmMain main = new frmMain();
                main.setVisible(true);
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu bị sai ! ");
    }

