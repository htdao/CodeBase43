    public void loadFile() {
        try {
            BufferedReader br = null;
            FileReader fr = null;
            fr = new FileReader("dangnhap.txt");
            br = new BufferedReader(fr);
            String s = null;

            try {
                while ((s = br.readLine()) != null) {

                    //Cắt chuỗi:
                    String arr[] = s.split("\t");
                    nhoTaiKhoan = Boolean.parseBoolean(arr[2]);
                    //Khởi tạo
                    if (nhoTaiKhoan) {
                        txt_nhoTaiKhoan.setSelected(true);
                        txt_userName.setText(arr[0]);
                        txt_passWord.setText(arr[1]);
                    }

                }
            } catch (IOException ex) {
                Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void saveFile() {

        try {
            BufferedWriter bw = null; //Khởi tạo
            FileWriter fw = null; //Khởi tạo
            String data = ""; //Tạo một string data bằng rỗng.
            if (txt_nhoTaiKhoan.isSelected()) {
                data = txt_userName.getText() + "\t" + txt_passWord.getText() + "\t" + txt_nhoTaiKhoan.isSelected();
            }
           
            fw = new FileWriter("dangnhap.txt");
            bw = new BufferedWriter(fw);
            bw.write(data);
            bw.close();
        } catch (IOException ex) {
            Logger.getLogger(frmNhanVienTemplate.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

